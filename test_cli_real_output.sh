# 颜色定义
GREEN='\033[0;32m'
BLUE='\033[0;34m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
NC='\033[0m'

test_count=0
passed=0

run_real_test() {
    local test_name="$1"
    local command="$2"
    local description="$3"
    
    test_count=$((test_count + 1))
    
    echo ""
    echo "═══════════════════════════════════════════════════════════════════════════"
    echo -e "${BLUE}📋 测试 ${test_count}: ${test_name}${NC}"
    echo "═══════════════════════════════════════════════════════════════════════════"
    echo ""
    echo -e "${YELLOW}💻 执行命令:${NC}"
    echo "   $ ${command}"
    echo ""
    echo -e "${CYAN}📝 说明: ${description}${NC}"
    echo ""
    echo -e "${YELLOW}📤 实际输出:${NC}"
    echo "---"
    
    # 实际执行命令
    eval "$command" 2>&1 | head -20
    
    echo "---"
    echo -e "${GREEN}✅ 测试完成${NC}"
    passed=$((passed + 1))
    sleep 0.3
}

